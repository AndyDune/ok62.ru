<?php

class Module_Catalogue_List_Sort_Set extends Dune_Include_Abstract_Code
{

    protected function code()
    {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        

     $this->field;
     if ($this->order == 'desc')
        $order = 'DESC';
     else 
        $order = '';
     
     if ($this->field == 'price')
        $field = 'price';
     else if ($this->field == 'space')
        $field = 'space';
     else 
        $field = false;
     
     $session = Dune_Session::getInstance('catalogue_list_order');
     $session->field = $field;
     $session->order = $order;
     $session->closeZone();


/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
    }
    
}
    
    