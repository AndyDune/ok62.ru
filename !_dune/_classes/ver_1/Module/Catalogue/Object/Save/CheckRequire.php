<?php

class Module_Catalogue_Object_Save_CheckRequire extends Dune_Include_Abstract_Code
{
    
    protected function code()
    {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
    $this->results['success'] = true;

    if (!$this->data['street'])
        $this->results['success'] = false;
    if ($this->type == 1)
    {
        if (!$this->data['rooms_count'])
            $this->results['success'] = false;
        if (!$this->data['floor'])
            $this->results['success'] = false;
        if (!$this->data['floors_total'])
            $this->results['success'] = false;
       
    }

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
    }
    
}
    
    