<?php

class Module_Catalogue_Mail_GetRequest extends Dune_Include_Abstract_Code
{

    protected function code()
    {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        

    $veiw = Dune_Zend_View::getInstance();
    
    $veiw->id = $this->id;
    $veiw->data = $this->data;
    
    $veiw->types = Special_Vtor_Types::$typesToRequest;
    
    if ($this->mode == 'add')
    {
        $name = 'mail/request/add';
        $topic = 'Новая заявка на покупку недвижимости. ok62.ru';
    }
    else 
    {
        $name = 'mail/request/edit';
        $topic = 'Отредактирована заявка на покупку недвижимости. ok62.ru';
    }
    
     $text = $veiw->render($name);
//    die(); //     !!!!!!!!     ТУТ ОТ 2009-0311

    
        $mail = new Module_Mail();
        $mail->body = $text;
        $mail->mail = Special_Vtor_Mail::$getRequest;
        $mail->name = '';
        $mail->subject = $topic;
        $mail->make();
    

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
    }
    
}
    
    