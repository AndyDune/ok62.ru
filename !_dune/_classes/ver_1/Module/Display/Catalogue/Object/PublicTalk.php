<?php

class Module_Display_Catalogue_Object_PublicTalk extends Dune_Include_Abstract_Code
{
    
    protected function code()
    {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        

        Dune_Static_StylesList::add('catalogue/info/pablic_talk_list');
        $talk = new Special_Vtor_Object_PublicTalkList($this->object->id);
        
        $view = Dune_Zend_View::getInstance();
        $view->count = $talk->count();
        $view->list = $talk->getListCumulate();
        echo $view->render('bit/catalogue/object/talk/list_simple');
        

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
    }
}
    
    