<?php

class Module_Display_Catalogue_Object_SaveToBasket extends Dune_Include_Abstract_Code
{

    protected function code()
    {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        

    $session = Dune_Session::getInstance(Dune_Session::ZONE_AUTH);
    $basket = new Special_Vtor_Object_User_Save($this->object->id);
    if ($basket->isSavedBy($session->user_id))
        echo '
        <div id="div-save-object-panel">
        <a id="a-save-buttom" href="/user/do/savefrombasket/' . $this->object->id . '/">Удалить</a>
        </div>
        ';
    else     
        echo '
        <div id="div-save-object-panel">
        <a id="a-save-buttom" href="/user/do/savetobasket/' . $this->object->id . '/">Отложить</a>
        </div>
        ';

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
    }
    
}
    
    