<?php

class Module_Pay_PageList extends Dune_Include_Abstract_Code
{

    protected function code()
    {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        

            $order_field = $this->order_field;
            $order_direction = $this->order_direction;
            $exit = false;

            $list_sell = new Special_Vtor_Object_List();
            $list_sell->setSeller($this->user->id);
            $list_sell->setPayMust(1);
            $list_sell->setOrder($order_field, $order_direction);
            $list_sell->setActivity(1);
            $count = $list_sell->count();
            $data = $list_sell->getListCumulate();

            $this->setResult('data',  $data);
            $this->setResult('count', $count);
            $this->setResult('exit', $exit);

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////        
    }
    
}
    
    